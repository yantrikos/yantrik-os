# BUILD ALL EXAMPLES
# ============================================================
def build_examples(stage):
    results = []
    idx = [0]  # mutable counter for name cycling

    def name():
        n = NAMES[idx[0] % len(NAMES)]
        idx[0] += 1
        return n

    def add(cat, user_msg, asst_msg, n):
        results.append({
            "conversations": [
                {"role": "system", "content": sys(stage, n)},
                {"role": "user", "content": user_msg},
                {"role": "assistant", "content": asst_msg.replace("{name}", n)},
            ],
            "metadata": {"bond_stage": stage, "scenario_type": cat},
        })

    def add_multi(cat, turns, n):
        msgs = [{"role": "system", "content": sys(stage, n)}]
        for asst, usr in turns:
            msgs.append({"role": "user", "content": usr})
            msgs.append({"role": "assistant", "content": asst.replace("{name}", n)})
        results.append({
            "conversations": msgs,
            "metadata": {"bond_stage": stage, "scenario_type": cat},
        })

    # Single-turn categories: (dict, category_name, target_count)
    singles = [
        (DAILY, "daily_planning", 60),
        (EMOTIONAL, "emotional_support", 60),
        (WORK, "work_advice", 60),
        (PROACTIVE, "proactive_suggestion", 50),
        (HONEST, "honest_feedback", 40),
        (CASUAL, "casual_conversation", 40),
        (MEMORY, "memory_recall", 40),
    ]

    for data, cat, target in singles:
        templates = data[stage]
        # templates are (assistant, user) pairs
        # Cycle through templates, varying names, to hit target
        for i in range(target):
            t = templates[i % len(templates)]
            n = name()
            # Format: (assistant_response, user_message)
            add(cat, t[1], t[0], n)

    # Multi-turn: 50
    multi_templates = MULTI[stage]
    for i in range(50):
        t = multi_templates[i % len(multi_templates)]
        n = name()
        add_multi("multi_turn", t, n)

    return results

