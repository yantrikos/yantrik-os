use serde::{Deserialize, Serialize};

// ══════════════════════════════════════════════════════════════════════════════
// § 1  Signal Types
// ══════════════════════════════════════════════════════════════════════════════

/// The four fundamental signal types that every brain observation is classified
/// into. Modeled after predictive processing / free energy minimization in
/// neuroscience — the brain cares about surprises, pressures, windows, and gaps.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum SignalType {
    /// Reality ≠ expected. Something deviated from the learned baseline.
    /// Examples: friend didn't reply (usually fast), spending spike, routine broken.
    PredictionError,

    /// Unresolved pressure accumulating. Needs action to relieve.
    /// Examples: overdue tasks piling up, inbox growing, conflict escalating.
    Tension,

    /// Favorable conditions detected. Time-bounded window to act.
    /// Examples: weather window for hike, birthday approaching, sale on wishlist item.
    Opportunity,

    /// Insufficient data to make a decision. Triggers external fetching.
    /// Examples: stale weather, no news in 6h, ambiguous scheduling conflict.
    Uncertainty,
}

impl SignalType {
    pub fn as_str(self) -> &'static str {
        match self {
            Self::PredictionError => "prediction_error",
            Self::Tension => "tension",
            Self::Opportunity => "opportunity",
            Self::Uncertainty => "uncertainty",
        }
    }

    /// Base weight for scoring. PredictionError is most salient (surprises
    /// demand attention), Uncertainty is lowest (just triggers info-seeking).
    pub fn base_weight(self) -> f64 {
        match self {
            Self::PredictionError => 1.0,
            Self::Tension => 0.85,
            Self::Opportunity => 0.75,
            Self::Uncertainty => 0.5,
        }
    }
}

