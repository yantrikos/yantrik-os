/// The output of a single brain_tick() — what the companion layer uses to
/// feed the urge queue.
#[derive(Debug, Clone)]
pub struct BrainTickResult {
    /// Candidates that passed scoring + orientation + threshold.
    pub emitted: Vec<BrainCandidate>,

    /// Number of candidates that were suppressed.
    pub suppressed_count: usize,

    /// Recommended interval until the next tick (seconds).
    pub next_tick_secs: f64,

    /// Whether the brain is "hungry" — external fetching should be triggered.
    pub should_fetch_external: bool,

    /// Current drive snapshot (for debugging/logging).
    pub drives_snapshot: Homeostasis,
}

/// Run a complete brain tick. This is the main entry point called by the
/// companion's think cycle.
///
/// # Arguments
/// - `state`: mutable brain state (persisted across ticks)
/// - `candidates`: all BrainCandidates collected this tick (wrapped instincts + detectors)
/// - `orientation_input`: context for the orientation step
/// - `max_emit`: maximum candidates to emit per tick (prevents flooding)
pub fn brain_tick(
    state: &mut BrainState,
    candidates: Vec<BrainCandidate>,
    orientation_input: &OrientationInput,
    max_emit: usize,
) -> BrainTickResult {
    state.tick_count += 1;
    let now = orientation_input.now;
    state.last_tick_at = now;

    // Decay recent counters every 10 ticks
    if state.tick_count % 10 == 0 {
        state.decay_recent_counts();
    }

    let threshold = state.drives.surfacing_threshold();
    let mut scored: Vec<BrainCandidate> = Vec::with_capacity(candidates.len());

    // Score all candidates
    for mut candidate in candidates {
        // Skip spam sources
        let source_key = candidate.source.source_key();
        if state.feedback.is_spam(&source_key, candidate.signal_type) {
            state.record_suppression();
            continue;
        }

        candidate.brain_score = score_candidate(&candidate, &state.drives, &state.feedback);
        scored.push(candidate);
    }

    // Sort by brain_score descending
    scored.sort_by(|a, b| b.brain_score.partial_cmp(&a.brain_score).unwrap_or(std::cmp::Ordering::Equal));

    // Orient and filter
    let mut emitted = Vec::new();
    let mut suppressed_count = 0;

    for mut candidate in scored {
        if emitted.len() >= max_emit {
            suppressed_count += 1;
            continue;
        }

        match orient(&candidate, orientation_input) {
            Some(note) if note.confirmed && note.adjusted_urgency >= threshold => {
                candidate.brain_score = note.adjusted_urgency;
                candidate.orientation = Some(note);
                state.record_emission(&candidate);
                emitted.push(candidate);
            }
            Some(_) | None => {
                suppressed_count += 1;
                state.record_suppression();
            }
        }
    }

    // Determine if external fetching should happen
    let should_fetch = state.drives.information_hunger > 0.6
        || (state.drives.information_hunger > 0.4
            && emitted.is_empty()
            && state.tick_count > 3);

    let next_tick = state.drives.scan_interval_secs();

    BrainTickResult {
        emitted,
        suppressed_count,
        next_tick_secs: next_tick,
        should_fetch_external: should_fetch,
        drives_snapshot: state.drives.clone(),
    }
}

