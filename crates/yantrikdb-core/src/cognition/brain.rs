// § 4  Source Feedback Tracker (EMA-based learning)
// ══════════════════════════════════════════════════════════════════════════════

/// Tracks per-source, per-signal-type feedback using Exponential Moving Average.
/// This is how the brain learns which sources produce useful signals.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SourceFeedback {
    /// Map from source_key → signal_type → EMA of usefulness (0.0–1.0).
    pub ema: HashMap<String, HashMap<String, f64>>,

    /// Total outcomes recorded per source (for confidence weighting).
    pub outcome_count: HashMap<String, u64>,
}

impl Default for SourceFeedback {
    fn default() -> Self {
        Self {
            ema: HashMap::new(),
            outcome_count: HashMap::new(),
        }
    }
}

impl SourceFeedback {
    /// EMA smoothing factor. Higher = more weight on recent outcomes.
    const ALPHA: f64 = 0.15;

    /// Default EMA for unknown source/signal combinations (mildly optimistic).
    const DEFAULT_EMA: f64 = 0.5;

    /// Minimum EMA before a source is considered "spam" (auto-suppressed).
    const SPAM_THRESHOLD: f64 = 0.1;

    /// Record an outcome for a source/signal combination.
    /// `reward`: 1.0 = acted on, 0.5 = read, 0.0 = ignored/dismissed.
    pub fn record(&mut self, source_key: &str, signal_type: SignalType, reward: f64) {
        let signal_str = signal_type.as_str().to_string();
        let current = self.get(source_key, signal_type);
        let new_ema = current * (1.0 - Self::ALPHA) + reward * Self::ALPHA;

        self.ema
            .entry(source_key.to_string())
            .or_default()
            .insert(signal_str, new_ema);

        *self.outcome_count
            .entry(source_key.to_string())
            .or_insert(0) += 1;
    }

    /// Get the current EMA for a source/signal combination.
    pub fn get(&self, source_key: &str, signal_type: SignalType) -> f64 {
        self.ema
            .get(source_key)
            .and_then(|m| m.get(signal_type.as_str()))
            .copied()
            .unwrap_or(Self::DEFAULT_EMA)
    }

    /// How many outcomes have been recorded for this source.
    pub fn confidence(&self, source_key: &str) -> u64 {
        self.outcome_count.get(source_key).copied().unwrap_or(0)
    }

    /// Is this source considered spam? (EMA below threshold with sufficient data)
    pub fn is_spam(&self, source_key: &str, signal_type: SignalType) -> bool {
        self.confidence(source_key) >= 5
            && self.get(source_key, signal_type) < Self::SPAM_THRESHOLD
    }
}

