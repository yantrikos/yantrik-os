// § 8  Brain State (persisted across ticks)
// ══════════════════════════════════════════════════════════════════════════════

/// The complete brain state persisted between ticks. Serialized to the DB
/// as a JSON blob in a cognitive_nodes entry or dedicated table.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BrainState {
    /// Homeostatic drives controlling rhythm and thresholds.
    pub drives: Homeostasis,

    /// Per-source feedback tracker for learning.
    pub feedback: SourceFeedback,

    /// Recent signal type distribution (for novelty tracking).
    pub recent_signal_counts: HashMap<String, u64>,

    /// Recent source distribution (for novelty tracking).
    pub recent_source_counts: HashMap<String, u64>,

    /// Number of brain ticks performed since startup.
    pub tick_count: u64,

    /// Timestamp of last tick.
    pub last_tick_at: f64,

    /// Timestamp of last external data event.
    pub last_data_event_at: f64,

    /// Total candidates emitted (lifetime).
    pub total_emitted: u64,

    /// Total candidates suppressed (lifetime).
    pub total_suppressed: u64,
}

impl Default for BrainState {
    fn default() -> Self {
        Self {
            drives: Homeostasis::default(),
            feedback: SourceFeedback::default(),
            recent_signal_counts: HashMap::new(),
            recent_source_counts: HashMap::new(),
            tick_count: 0,
            last_tick_at: 0.0,
            last_data_event_at: 0.0,
            total_emitted: 0,
            total_suppressed: 0,
        }
    }
}

impl BrainState {
    /// Compute source diversity (0.0 = all same source, 1.0 = uniform distribution).
    /// Uses Shannon entropy normalized by log(N).
    pub fn source_diversity(&self) -> f64 {
        let total: u64 = self.recent_source_counts.values().sum();
        if total == 0 {
            return 0.0;
        }
        let n = self.recent_source_counts.len();
        if n <= 1 {
            return 0.0;
        }

        let total_f = total as f64;
        let entropy: f64 = self.recent_source_counts.values()
            .filter(|&&c| c > 0)
            .map(|&c| {
                let p = c as f64 / total_f;
                -p * p.ln()
            })
            .sum();

        let max_entropy = (n as f64).ln();
        if max_entropy == 0.0 { 0.0 } else { entropy / max_entropy }
    }

    /// Record that a candidate was emitted to the urge queue.
    pub fn record_emission(&mut self, candidate: &BrainCandidate) {
        let signal_key = candidate.signal_type.as_str().to_string();
        let source_key = candidate.source.source_key();

        *self.recent_signal_counts.entry(signal_key).or_insert(0) += 1;
        *self.recent_source_counts.entry(source_key).or_insert(0) += 1;
        self.total_emitted += 1;
    }

    /// Record that a candidate was suppressed.
    pub fn record_suppression(&mut self) {
        self.total_suppressed += 1;
    }

    /// Decay recent counters to prevent them from growing unbounded.
    /// Called periodically (e.g., every 10 ticks or every 10 minutes).
    pub fn decay_recent_counts(&mut self) {
        // Halve all counts — exponential decay
        self.recent_signal_counts.retain(|_, v| {
            *v /= 2;
            *v > 0
        });
        self.recent_source_counts.retain(|_, v| {
            *v /= 2;
            *v > 0
        });
    }

    /// Update all homeostatic drives from the current state.
    pub fn update_drives(&mut self, now: f64, unresolved_tension_count: usize) {
        let dt = now - self.drives.last_updated;
        if dt <= 0.0 {
            return;
        }

        // Natural decay toward resting state
        self.drives.decay(dt);

        // Information hunger from time since last data event
        let secs_since_data = now - self.last_data_event_at;
        self.drives.update_information_hunger(secs_since_data);

        // Novelty hunger from source diversity
        let diversity = self.source_diversity();
        self.drives.update_novelty_hunger(diversity);

        // Tension pressure from unresolved count
        self.drives.update_tension_pressure(unresolved_tension_count);

        // Usefulness pressure is updated via record_feedback(), not here

        self.drives.last_updated = now;
    }
}

