/// Per-instinct default signal type mappings. Instincts that don't appear
/// here fall through to keyword heuristics.
static INSTINCT_SIGNAL_DEFAULTS: &[(&str, SignalType)] = &[
    // PredictionError — something deviated from expected
    ("contact_silence", SignalType::PredictionError),
    ("pattern_break", SignalType::PredictionError),
    ("routine_deviation", SignalType::PredictionError),
    ("spending_anomaly", SignalType::PredictionError),
    ("sleep_anomaly", SignalType::PredictionError),
    ("mood_shift", SignalType::PredictionError),
    ("energy_shift", SignalType::PredictionError),
    ("activity_reflector", SignalType::PredictionError),

    // Tension — unresolved pressure
    ("open_loop", SignalType::Tension),
    ("overdue_task", SignalType::Tension),
    ("conflict_alerting", SignalType::Tension),
    ("cognitive_load", SignalType::Tension),
    ("deadline_pressure", SignalType::Tension),
    ("inbox_pressure", SignalType::Tension),
    ("decision_fatigue", SignalType::Tension),
    ("unprocessed_backlog", SignalType::Tension),
    ("goal_drift", SignalType::Tension),
    ("health_check", SignalType::Tension),

    // Opportunity — favorable window
    ("weather_window", SignalType::Opportunity),
    ("birthday_reminder", SignalType::Opportunity),
    ("activity_recommender", SignalType::Opportunity),
    ("connection_weaver", SignalType::Opportunity),
    ("learning_moment", SignalType::Opportunity),
    ("habit_streak", SignalType::Opportunity),
    ("bond_milestone", SignalType::Opportunity),
    ("gratitude", SignalType::Opportunity),
    ("skill_gap", SignalType::Opportunity),
    ("serendipity", SignalType::Opportunity),
    ("momentum", SignalType::Opportunity),
    ("growth_edge", SignalType::Opportunity),

    // Uncertainty — need more data
    ("stale_data", SignalType::Uncertainty),
    ("context_bridge", SignalType::Uncertainty),
    ("check_in", SignalType::Uncertainty),
    ("curiosity", SignalType::Uncertainty),
    ("exploration", SignalType::Uncertainty),
];

/// Keyword patterns for signal type inference when no explicit mapping exists.
static KEYWORD_PATTERNS: &[(&[&str], SignalType)] = &[
    (&["overdue", "missed", "late", "forgot", "anomaly", "unusual", "unexpected", "deviat"],
     SignalType::PredictionError),
    (&["pending", "backlog", "conflict", "pressure", "overload", "stuck", "blocked", "urgent"],
     SignalType::Tension),
    (&["birthday", "weather", "window", "opportunity", "sale", "streak", "milestone", "ready"],
     SignalType::Opportunity),
    (&["stale", "unknown", "missing", "check", "fetch", "update", "refresh", "unclear"],
     SignalType::Uncertainty),
];

/// Infer the signal type for a wrapped instinct output using a 3-layer heuristic:
///
/// 1. **Explicit metadata**: if the context JSON contains a `signal_type` field, use it.
/// 2. **Per-instinct default**: lookup in `INSTINCT_SIGNAL_DEFAULTS`.
/// 3. **Keyword heuristics**: scan reason + instinct_name for signal keywords.
/// 4. **Fallback**: `Uncertainty` (safest default — triggers info-seeking, not action).
pub fn infer_signal_type(
    instinct_name: &str,
    reason: &str,
    context: &serde_json::Value,
) -> SignalType {
    // Layer 1: Explicit metadata in context
    if let Some(st) = context.get("signal_type").and_then(|v| v.as_str()) {
        match st {
            "prediction_error" => return SignalType::PredictionError,
            "tension" => return SignalType::Tension,
            "opportunity" => return SignalType::Opportunity,
            "uncertainty" => return SignalType::Uncertainty,
            _ => {}
        }
    }

    // Layer 2: Per-instinct default
    for &(name, signal) in INSTINCT_SIGNAL_DEFAULTS {
        if instinct_name == name || instinct_name.contains(name) {
            return signal;
        }
    }

    // Layer 3: Keyword heuristics on reason + instinct_name
    let haystack = format!("{} {}", instinct_name, reason).to_lowercase();
    let mut best_match: Option<(usize, SignalType)> = None;

    for &(keywords, signal) in KEYWORD_PATTERNS {
        let hits = keywords.iter()
            .filter(|kw| haystack.contains(**kw))
            .count();
        if hits > 0 {
            if best_match.map_or(true, |(best, _)| hits > best) {
                best_match = Some((hits, signal));
            }
        }
    }

    if let Some((_, signal)) = best_match {
        return signal;
    }

    // Layer 4: Fallback
    SignalType::Uncertainty
}

// ══════════════════════════════════════════════════════════════════════════════
// § 6  Candidate Scoring
// ══════════════════════════════════════════════════════════════════════════════

/// Score a BrainCandidate using the signal type weights, homeostatic drive
/// modulation, and source feedback EMA.
pub fn score_candidate(
    candidate: &BrainCandidate,
    drives: &Homeostasis,
    feedback: &SourceFeedback,
) -> f64 {
    let signal_weight = candidate.signal_type.base_weight();
    let source_key = candidate.source.source_key();

    // Drive modulation: each drive amplifies its related signal types
    let drive_boost = match candidate.signal_type {
        SignalType::PredictionError => 1.0 + drives.novelty_hunger * 0.3,
        SignalType::Tension => 1.0 + drives.tension_pressure * 0.4,
        SignalType::Opportunity => 1.0 + drives.information_hunger * 0.2,
        SignalType::Uncertainty => 1.0 + drives.information_hunger * 0.5,
    };

    // Source EMA: learned reliability of this source for this signal type
    let source_ema = feedback.get(&source_key, candidate.signal_type);

    // Confidence weight: new sources get benefit of doubt, established ones
    // are weighted by their track record
    let confidence = feedback.confidence(&source_key);
    let confidence_weight = if confidence < 3 {
        0.8 // new source — slight discount but not harsh
    } else {
        source_ema
    };

    // Final score
    let score = candidate.raw_urgency
        * signal_weight
        * drive_boost
        * confidence_weight;

    score.clamp(0.0, 1.0)
}

// ══════════════════════════════════════════════════════════════════════════════
// § 7  Orientation (cheap context verification)
// ══════════════════════════════════════════════════════════════════════════════

/// Orientation check results — cheap verification that a signal is still valid.
pub struct OrientationInput {
    /// Current timestamp.
    pub now: f64,
    /// Seconds since last user interaction.
    pub idle_secs: f64,
    /// Number of pending urges already in the queue.
    pub pending_urge_count: usize,
    /// Currently active cooldown keys.
    pub active_cooldowns: Vec<String>,
    /// Current hour of day (0-23) for time-relevance checks.
    pub current_hour: u32,
}

/// Run the cheap orientation step on a scored candidate. This verifies the
/// signal is still valid without needing an LLM call. Returns None if the
/// candidate should be dropped entirely.
pub fn orient(
    candidate: &BrainCandidate,
    input: &OrientationInput,
) -> Option<OrientationNote> {
    // Check 1: Cooldown — if this cooldown key is active, suppress
    if input.active_cooldowns.contains(&candidate.cooldown_key) {
        return Some(OrientationNote {
            confirmed: false,
            note: "Cooldown active for this signal".into(),
            adjusted_urgency: 0.0,
        });
    }

    // Check 2: Queue saturation — if too many pending urges, raise bar
    let queue_penalty = if input.pending_urge_count > 5 {
        0.8 // penalize when queue is full
    } else {
        1.0
    };

    // Check 3: Time relevance — some signals are less relevant at night
    let time_factor = match candidate.signal_type {
        SignalType::Opportunity => {
            // Opportunities about activities are less relevant at 2am
            if input.current_hour >= 23 || input.current_hour < 6 {
                0.5
            } else {
                1.0
            }
        }
        SignalType::Tension => {
            // Tensions can wait until morning
            if input.current_hour >= 23 || input.current_hour < 7 {
                0.6
            } else {
                1.0
            }
        }
        SignalType::PredictionError => 1.0, // surprises are always relevant
        SignalType::Uncertainty => {
            // Don't trigger fetches at night
            if input.current_hour >= 22 || input.current_hour < 6 {
                0.3
            } else {
                1.0
            }
        }
    };

    // Check 4: Staleness — candidates older than 10 minutes lose relevance
    let age = input.now - candidate.created_at;
    let freshness = if age > 600.0 {
        (1.0 - (age - 600.0) / 1200.0).max(0.1)
    } else {
        1.0
    };

    let adjusted = candidate.brain_score * queue_penalty * time_factor * freshness;

    if adjusted < 0.05 {
        return None; // drop entirely — too weak after orientation
    }

    Some(OrientationNote {
        confirmed: adjusted > 0.15,
        note: format!(
            "queue={:.1} time={:.1} fresh={:.2}",
            queue_penalty, time_factor, freshness
        ),
        adjusted_urgency: adjusted,
    })
}

