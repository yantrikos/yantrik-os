// § 2  Brain Candidate
// ══════════════════════════════════════════════════════════════════════════════

/// A candidate observation from the brain's scan — either wrapped from an
/// instinct UrgeSpec or generated by a native Detector.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BrainCandidate {
    /// Unique ID for deduplication and feedback tracking.
    pub candidate_id: String,

    /// Where this candidate came from.
    pub source: CandidateSource,

    /// Classified signal type (inferred or explicit).
    pub signal_type: SignalType,

    /// Raw urgency from the source (0.0–1.0).
    pub raw_urgency: f64,

    /// Computed brain score after drive modulation and source EMA.
    pub brain_score: f64,

    /// Human-readable reason for this signal.
    pub reason: String,

    /// Suggested message/action text.
    pub suggested_message: String,

    /// Optional action identifier (tool call, fetch, etc.).
    pub action: Option<String>,

    /// Structured context payload.
    pub context: serde_json::Value,

    /// Cooldown key for deduplication.
    pub cooldown_key: String,

    /// Orientation notes added during the orient step.
    pub orientation: Option<OrientationNote>,

    /// Timestamp when this candidate was generated.
    pub created_at: f64,
}

/// Where a BrainCandidate originated.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum CandidateSource {
    /// Wrapped from an existing instinct's UrgeSpec output.
    Instinct {
        instinct_name: String,
        /// Original instinct category (for signal type inference).
        category: String,
    },
    /// Generated by a native brain Detector (pattern break, forward projection, etc.).
    Detector {
        detector_name: String,
    },
    /// External data fetch triggered by Uncertainty signal.
    ExternalFetch {
        fetch_type: String,
    },
}

impl CandidateSource {
    pub fn source_key(&self) -> String {
        match self {
            Self::Instinct { instinct_name, .. } => format!("instinct:{instinct_name}"),
            Self::Detector { detector_name } => format!("detector:{detector_name}"),
            Self::ExternalFetch { fetch_type } => format!("fetch:{fetch_type}"),
        }
    }
}

/// Context notes added during the cheap orientation step — verifies the
/// signal is still valid without needing an LLM call.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct OrientationNote {
    /// Was the signal confirmed as still valid after context check?
    pub confirmed: bool,
    /// Brief explanation of why the signal was confirmed or rejected.
    pub note: String,
    /// Adjusted urgency after orientation (may increase or decrease).
    pub adjusted_urgency: f64,
}

// ══════════════════════════════════════════════════════════════════════════════
// § 3  Homeostatic Drives
// ══════════════════════════════════════════════════════════════════════════════

/// The four homeostatic drives that control the brain's rhythm. Each oscillates
/// between 0.0 (sated) and 1.0 (hungry/pressured), modulating what signals
/// pass the scoring threshold.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Homeostasis {
    /// Rises when no new data arrives. Decays on fresh input.
    /// High → brain becomes more interested in any information.
    pub information_hunger: f64,

    /// Rises when signals are repetitive / same sources dominate.
    /// High → brain weights novel sources and signal types higher.
    pub novelty_hunger: f64,

    /// Rises with unresolved tensions (overdue tasks, conflicts, etc.).
    /// High → Tension signals get priority, brain focuses on resolution.
    pub tension_pressure: f64,

    /// Rises when the user ignores or dismisses outputs.
    /// High → brain RAISES threshold (becomes more selective, not spammier).
    pub usefulness_pressure: f64,

    /// Timestamp of last drive update.
    pub last_updated: f64,
}

impl Default for Homeostasis {
    fn default() -> Self {
        Self {
            information_hunger: 0.3,   // slightly hungry on startup
            novelty_hunger: 0.3,
            tension_pressure: 0.1,     // assume calm start
            usefulness_pressure: 0.2,  // slightly cautious by default
            last_updated: 0.0,
        }
    }
}

impl Homeostasis {
    /// Compute the adaptive surfacing threshold. Higher usefulness_pressure
    /// means higher threshold → fewer signals pass → more selective.
    /// Range: 0.25 (very permissive) to 0.85 (very selective).
    pub fn surfacing_threshold(&self) -> f64 {
        let base = 0.35;
        let usefulness_boost = self.usefulness_pressure * 0.5;
        (base + usefulness_boost).clamp(0.25, 0.85)
    }

    /// Compute the desired scan interval in seconds based on drive volatility.
    /// High volatility (active drives) → faster scanning (30s).
    /// Low volatility (all calm) → slower scanning (300s).
    pub fn scan_interval_secs(&self) -> f64 {
        let volatility = self.drive_volatility();
        // Map volatility [0, 1] → interval [300, 30]
        let fast = 30.0;
        let slow = 300.0;
        slow - (volatility * (slow - fast))
    }

    /// Overall drive volatility — how "activated" the brain is.
    /// Average of all drives, weighted by their salience.
    pub fn drive_volatility(&self) -> f64 {
        let weighted = self.information_hunger * 0.3
            + self.novelty_hunger * 0.2
            + self.tension_pressure * 0.3
            + self.usefulness_pressure * 0.2;
        weighted.clamp(0.0, 1.0)
    }

    /// Update information hunger based on seconds since last data event.
    /// Rises exponentially as time without new data increases.
    pub fn update_information_hunger(&mut self, secs_since_last_event: f64) {
        // Half-life: hunger reaches 0.5 at ~600s (10min) of no data
        let hunger = 1.0 - (-secs_since_last_event / 900.0).exp();
        self.information_hunger = hunger.clamp(0.0, 1.0);
    }

    /// Feed information hunger — decays toward 0 when fresh data arrives.
    pub fn feed_information(&mut self, amount: f64) {
        self.information_hunger = (self.information_hunger - amount).clamp(0.0, 1.0);
    }

    /// Update novelty hunger based on the diversity of recent signal sources.
    /// Low diversity → hunger rises. High diversity → hunger falls.
    pub fn update_novelty_hunger(&mut self, source_diversity: f64) {
        // source_diversity: 0.0 (all same source) to 1.0 (all different)
        self.novelty_hunger = (1.0 - source_diversity).clamp(0.0, 1.0);
    }

    /// Update tension pressure from the count of unresolved tensions.
    pub fn update_tension_pressure(&mut self, unresolved_count: usize) {
        // Saturates around 10 unresolved tensions
        let pressure = 1.0 - (-0.2 * unresolved_count as f64).exp();
        self.tension_pressure = pressure.clamp(0.0, 1.0);
    }

    /// Update usefulness pressure based on recent acceptance rate.
    /// acceptance_rate: 0.0 (everything ignored) to 1.0 (everything acted on).
    pub fn update_usefulness(&mut self, acceptance_rate: f64) {
        // Invert: low acceptance → high pressure
        self.usefulness_pressure = (1.0 - acceptance_rate).clamp(0.0, 1.0);
    }

    /// Decay all drives toward their resting state over a time delta.
    /// Called each brain tick to prevent drives from sticking at extremes.
    pub fn decay(&mut self, dt_secs: f64) {
        let decay_rate = 0.001; // per second — full decay ~16min
        let factor = (-decay_rate * dt_secs).exp();

        // Decay toward resting values
        self.information_hunger = lerp(0.2, self.information_hunger, factor);
        self.novelty_hunger = lerp(0.2, self.novelty_hunger, factor);
        self.tension_pressure = lerp(0.05, self.tension_pressure, factor);
        self.usefulness_pressure = lerp(0.15, self.usefulness_pressure, factor);
    }
}

