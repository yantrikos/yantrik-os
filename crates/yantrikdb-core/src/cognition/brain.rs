//! Autonomous Brain — data-driven thinking without LLM involvement.
//!
//! The brain is the DB's own "consciousness": it scans its data for interesting
//! signals, scores them, orients for context, and emits candidates that the
//! companion layer converts into urges. When nothing interesting is found
//! internally, homeostatic drives push the system to seek external information
//! (RSS, weather, news) — like a human turning on the TV when bored.
//!
//! # Signal Model
//!
//! Every observation is classified into one of four signal types:
//!
//! - **PredictionError**: reality ≠ expected (friend usually replies within 1h,
//!   it's been 6h; budget spending rate jumped)
//! - **Tension**: unresolved pressure building (3 overdue tasks, conflict count
//!   rising, inbox growing faster than processing)
//! - **Opportunity**: favorable conditions detected (weather window for planned
//!   hike, contact's birthday approaching, price drop on watched item)
//! - **Uncertainty**: insufficient data to decide — triggers external fetching
//!   (stale weather data, no news in 6h, missing context for a decision)
//!
//! # Homeostatic Drives
//!
//! Four drives oscillate between satiation and hunger, controlling the system's
//! rhythm and preventing both spam and stagnation:
//!
//! - `information_hunger`: rises when no new data arrives, decays on fresh input
//! - `novelty_hunger`: rises when signals are repetitive, decays on novel patterns
//! - `tension_pressure`: rises with unresolved tensions, decays as they resolve
//! - `usefulness_pressure`: rises when outputs are ignored, raises threshold
//!   (more selective, NOT more spammy)
//!
//! # Architecture
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────┐
//! │                    brain_tick()                          │
//! │                                                         │
//! │  1. Update homeostatic drives from recent events        │
//! │  2. Collect BrainCandidates from:                       │
//! │     a. Wrapped instinct UrgeSpecs (via signal_type_of)  │
//! │     b. Native Detectors (pattern break, forward proj.)  │
//! │  3. Score candidates (signal × drives × source_ema)     │
//! │  4. Orient top candidates (cheap context enrichment)    │
//! │  5. Apply adaptive threshold (driven by usefulness)     │
//! │  6. Emit survivors → urge queue                         │
//! │  7. Adjust scan interval from drive volatility          │
//! └─────────────────────────────────────────────────────────┘
//! ```

